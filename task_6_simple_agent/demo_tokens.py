"""
Демонстрация работы подсчета токенов и симуляции лимита контекста.
Показывает рост входных токенов и что происходит при переполнении.

Примеры промптов:
Привет! Что ты можешь делать?
Расскажи о себе кратко.
Напиши подробный фантастический рассказ о первом контакте человечества с кремниевой формой жизни на далекой планете. Рассказ должен состоять из трех глав: 1. Экспедиция и открытие, 2. Попытки установить языковой барьер и недопонимание, 3. Финал, где обе расы находят общий язык через математику. Сделай текст максимально живым, с глубокими диалогами персонажей, описанием окружения и внутренними переживаниями капитана корабля.
"""

from agent import DeepSeekAgent
import os


def demo():
    """
    Демонстрирует поведение агента при росте использования контекста.
    Для демо используется очень низкий лимит (1000 токенов).
    """

    # Проверяем наличие API ключа
    if not os.environ.get("DEEPSEEK_API_KEY"):
        print("❌ Ошибка: Не установлена переменная окружения DEEPSEEK_API_KEY")
        print("Установите её перед запуском демо:")
        print("  export DEEPSEEK_API_KEY='your-key-here'")
        return

    print("=" * 70)
    print("ДЕМОНСТРАЦИЯ: Подсчет токенов и лимит контекста")
    print("=" * 70)
    print()

    agent = DeepSeekAgent(
        system_prompt="Ты помощник. Отвечай кратко на русском языке."
    )

    # Сценарий: несколько коротких сообщений, затем длинное сообщение
    messages = [
        "Привет! Что ты можешь делать?",
        "Какой сегодня день недели?",
        "Расскажи о себе кратко.",
        "Напиши длинный текст про искусственный интеллект. " * 20,  # Длинное сообщение
    ]

    print(f"Лимит контекста для демо: {agent.max_context_tokens} токенов\n")

    for i, msg in enumerate(messages, 1):
        msg_display = msg[:100] + "..." if len(msg) > 100 else msg
        print(f"📝 Сообщение {i}: '{msg_display}'")
        print("-" * 70)

        try:
            response, metrics = agent.send_message(msg)

            # Выводим сокращенный ответ
            resp_display = response[:150] + "..." if len(response) > 150 else response
            print(f"✓ Ответ: {resp_display}\n")

            # Выводим метрики
            print("[Метрики этого шага]")
            print(f"  • Текущий запрос: {metrics['current_query_tokens']} токенов")
            print(f"  • Контекст до ответа: {metrics['history_tokens_before_response']} токенов")
            print(f"  • Ответ модели: {metrics['completion_tokens_used']} токенов")
            print(f"  • Всего за шаг: {metrics['total_this_step']} токенов")

            # Статус использования
            status = agent.get_status()
            print(f"\n[Статус контекста после этого шага]")
            print(f"  • Использовано: {status['used']} / {status['limit']} токенов ({status['percent']:.1f}%)")
            print(f"  • Сообщений: {status['messages_count']}")

            if status['percent'] > 80:
                print(f"  ⚠ ВНИМАНИЕ: Контекст заполнен на {status['percent']:.0f}%!")

            print()
            print("=" * 70)
            print()

        except ValueError as e:
            print(f"❌ ОШИБКА: {e}\n")
            print("[Попытка срабатывания лимита контекста]")
            print(f"  Сообщение не было добавлено в историю (откат произошел автоматически)")
            print(f"  Агент защищен от переполнения контекста\n")

            # Показываем финальный статус
            status = agent.get_status()
            print(f"[Финальный статус контекста]")
            print(f"  • Использовано: {status['used']} / {status['limit']} токенов ({status['percent']:.1f}%)")
            print(f"  • История осталась нетронутой\n")
            break


if __name__ == "__main__":
    demo()
